// SmoothNAS Dutch (nl) locale bundle. Phase 3 of the i18n proposal.
//
// Mirrors every key in en.ts (the source of truth). When a key is added
// to en.ts, it must be added here too — Phase 5 introduces a CI gate
// that fails the build on key drift.
//
// Translation conventions:
//   - Storage / protocol identifiers (RAID-5, RAIDZ-1, mdadm, ZFS,
//     iSCSI, NFS, SMB, IQN, CIDR, MTU, VLAN, DHCP, etc.) stay literal —
//     they're spoken as English in Dutch IT shops.
//   - Common verbs (Save / Cancel / Apply / Delete) are translated.
//   - User-facing nouns (Network / Settings / Users / Disks) are
//     translated where the Dutch form is idiomatic.
//   - Technical jargon that has no clean Dutch equivalent (Pool, Array,
//     Snapshot, Tier, Backend, Cluster, Cache, Mount, Stripe, Mirror,
//     Bond, Pin, Drain, Stage) stays English.

